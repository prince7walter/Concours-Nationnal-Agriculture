package com.concours.entity.lists;

public class visiteList {
    String id_visite, candidat, date, heure, juge, etat ;

    public String getId_visite() {
        return id_visite;
    }

    public void setId_visite(String id_visite) {
        this.id_visite = id_visite;
    }

    public String getCandidat() {
        return candidat;
    }

    public void setCandidat(String candidat) {
        this.candidat = candidat;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getHeure() {
        return heure;
    }

    public void setHeure(String heure) {
        this.heure = heure;
    }

    public String getJuge() {
        return juge;
    }

    public void setJuge(String juge) {
        this.juge = juge;
    }

    public String getEtat() {
        return etat;
    }

    public void setEtat(String etat) {
        this.etat = etat;
    }
}
